    private void btn_detallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_detallesActionPerformed
        try {
            if (JOptionPane.showConfirmDialog(this, "Â¿Desea ver los detalles de este inmueble?") == JOptionPane.YES_OPTION) {
                int id;
                id = Integer.parseInt(modelo.getValueAt(tbe_listaInmuebles.getSelectedRow(), 0).toString());
                ci.consultarInmuebleId(id);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar primero un inmueble");
        }
    }//GEN-LAST:event_btn_detallesActionPerformed

    private void txt_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_buscarActionPerformed
        TableRowSorter<DefaultTableModel> Sorter;
        Sorter = new TableRowSorter<>(modelo);
        tbe_listaInmuebles.setRowSorter(Sorter);
        Sorter.setRowFilter(RowFilter.regexFilter(txt_buscar.getText(), cbx_filtro.getSelectedIndex()));
    }//GEN-LAST:event_txt_buscarActionPerformed

    private void btn_citaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_citaActionPerformed
        Registro_cita AG = new Registro_cita();
        AG.setVisible(true);
        AG.setIdInmueble(modelo.getValueAt(tbe_listaInmuebles.getSelectedRow(), 0).toString());
        AG.setLocationRelativeTo(this);
    }//GEN-LAST:event_btn_citaActionPerformed

